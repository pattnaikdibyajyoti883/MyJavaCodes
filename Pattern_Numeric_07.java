package patternprograms;

public class Pattern_Numeric_07 {

	public static void main(String[] args) 
	{
		int count=0;
		for (int i = 1; i <=6; i++) 
		{
			for (int j = 1; j <=4; j++) 
			{
				count=count+1;
				System.out.print(count+" ");
			}
			System.out.println();
		}
	}

}
